#include "libft.h"

int		ft_skip(const char *str)
{
	int	i;

	i = 0;
	while (*(str + i) == '\n' ||
	*(str + i) == '\t' ||
	*(str + i) == '\r' ||
	*(str + i) == '\v' ||
	*(str + i) == '\f' ||
	*(str + i) == 32)
		i++;
	return (i);
}
